  <h2 class="font-extrabold text-2xl leading-9 my-8">
    Need for short term mitigation measures and longer-term structural reforms
  </h2>
  <p class="mb-6">
    Given the many challenges facing the media with regard to Big Tech and AI, the Policy Manual
    offers a <b>toolbox of potential policy interventions</b> to ensure the availability and
    accessibility of public interest information and
    <b>foster a healthier online information ecosystem</b>. Building on in-depth analysis of
    existing initiatives and experiences as well as broad expert consultations, it provides
    <b>short-term practical mitigation measures</b>
    to strengthen media visibility, viability, and the safety of journalists online, while also calling
    for <b>longer-term</b> <b>structural changes</b> aimed at building resilient digital information
    spaces conducive to media freedom, peace, and security.
  </p>
  <p class="mb-6">
    While <b>short-term measures</b> are necessary to address current harms and challenges, the
    Policy Manual concludes that they alone are
    <b>not sufficient to overcome excessive concentration of power</b>
    and the control which tech platforms exert on the media. It thus suggests structural changes to support
    states in <b>transforming the current digital information environment</b> into a diverse, independent
    and resilient online information ecosystem designed to serve the public interest and promote media
    freedom, while reinforcing democratic processes.
  </p>
  <p class="mb-6">
    Acknowledging the diversity of legal systems and societal contexts across the OSCE, the Policy
    Manual does not prescribe a one-size-fits-all solution. The effectiveness and appropriateness of
    policy interventions depend on the specific political, legal and institutional context in which
    they are implemented. Hence, the Policy Manual underscores the need for policies to be <b
      >rooted in the rule of law</b
    >, adhere to <b>international human rights standards</b>, and be developed through
    <b>inclusive</b>, <b>transparent</b>, and <b>multi-stakeholder due diligence processes</b>.
  </p>

